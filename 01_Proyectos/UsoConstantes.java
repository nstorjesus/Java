
public class UsoConstantes {

    public static void main(String args[]) {
      
        //para declarar una constante se utiliza la palabra final + tipo de valor
        
        final int ejemplo; //Tambien se puede declarar e inicializar en la misma linea (lo normal 
                           //es que lo hagas en la misma linea ya que una constante tiene un valor fijo
                           //y lo normal es que ya sepas el valor de esa constante
        
        ejemplo=9;
        
        System.out.println(ejemplo);
        
        
        
    }
}
